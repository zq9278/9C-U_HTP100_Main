// 串口空闲中断回调函数
void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size) {
    if (huart->Instance == USART2) {
        HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_6);
        uart_data *pUartData = &uart_rx_data_t[uart_buff_ctrl];
        pUartData->length = Size;
        // 发送接收数据到队列
        xQueueSendFromISR(UART_DMA_IDLE_RECEPT_QUEUEHandle, &pUartData, NULL);
        // 切换缓冲区索引
        uart_buff_ctrl++;
        uart_buff_ctrl %= UART_BUFFER_QUANTITY;
        // 重新启动DMA接收
        HAL_UARTEx_ReceiveToIdle_DMA(&huart2, uart_rx_data_t[uart_buff_ctrl].buffer, UART_RX_BUFFER_SIZE);
    }

}
void HAL_UART_ErrorCallback(UART_HandleTypeDef *huart) {
  if (huart->Instance == USART2) {
    uint32_t error = HAL_UART_GetError(huart);
    // 根据错误类型处理
    if (error & HAL_UART_ERROR_ORE) {
      printf("UART 溢出错误\n");
      __HAL_UART_CLEAR_OREFLAG(huart); // 清除溢出标志
      while (__HAL_UART_GET_FLAG(huart, UART_FLAG_RXNE)) {
        (huart->Instance->RDR & 0xFF); // 读取接收寄存器以清空缓冲区
      }
    }
    if (error & HAL_UART_ERROR_FE) {
      printf("UART 帧错误\n");
      __HAL_UART_CLEAR_FEFLAG(huart); // 清除帧错误标志
    }
    if (error & HAL_UART_ERROR_NE) {
      printf("UART 噪声错误\n");
      __HAL_UART_CLEAR_NEFLAG(huart); // 清除噪声错误标志
    }
    if (error & HAL_UART_ERROR_DMA) {
      printf("UART DMA 错误\n");
      HAL_DMA_Abort(huart->hdmarx); // 停止 DMA
    }
    // 重置 UART 外设状态
   // MX_USART2_UART_Init();
    // 尝试重新启动 DMA 接收
    if (HAL_UARTEx_ReceiveToIdle_DMA(&huart2, uart2_data.buffer, sizeof(uart2_data.buffer)) == HAL_OK) {
      printf("重新启动 DMA 接收成功\n");
    } else {
      printf("重新启动 DMA 接收失败\n");
    }
  }
}

